"""
See documentation for more on patterns
0 is an unnocupied cell.
1 is cell of the player 1.
2 is cell of the player 2.

This pattern on the board translates to the tuple:
 54 
0.3  -> (0,1,2,3,4,5) The middle cell is considered empty.
12

For each local pattern save E(pattern),IE(pattern)

Each of the values of a pattern are calculated assuming a determinate player  will play on the '.'
and then analizing the results.

Function E(pattern), called explotition of the pattern is defined
as the amount of disjoint groups of 1s joined by putting a 1 in the '.'
E[pattern] = {0,1,2}
Is 2 for patterns like this:
     10
    0.1
    10
Is 1 for patterns like this:
     01
    0.0
    01
Is 0 for patterns like this:
     00
    0.1
    01

Function IE(pattern), called inverse explotition of the pattern, is defined
as the amount of disjoint groups of 2s blocked for more than 1 movement by
putting a 1 in the '.'
IE[pattern] = {0,1}
Is 1 for patterns like this:
     21
    0.2
    00
Is 0 for patterns like this: (note that by playing to the left of the . player 2 connects its cells in 1 movement)
     20
    0.0
    20

This functions were defined assuming 1 was playing, but there are analogous for te case of 2.
"""

def e(pattern:tuple[int,int,int,int,int,int],player:int)->int:
    """
    Calculate e(pattern) considering the player who made the move.
    Player must be in {1,2}
    """
    return patterns_for_player_1[pattern][0] if player == 1 else patterns_for_player_2[pattern][0]

